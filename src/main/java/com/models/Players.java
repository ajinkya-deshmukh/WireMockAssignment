package com.models;

public class Players {
	
	private String team;
	private String name;
	private int auction_cost;
	
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAuction_cost() {
		return auction_cost;
	}
	public void setAuction_cost(int auction_cost) {
		this.auction_cost = auction_cost;
	}
}
